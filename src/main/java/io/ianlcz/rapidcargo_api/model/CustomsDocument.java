package io.ianlcz.rapidcargo_api.model;

public class CustomsDocument {
    private String type;
    private String ref;

    // Getters and setters
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }
}

